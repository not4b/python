# Variable names 
# A variable can have a short name (like x and y) or more descriptive name (age, carname, total_volume). Rules for python variables:
# A variable name must start with a letter or the underscore character 
# A variable name cannort start with a number 
# A variable name can only contain alpha-numeric characters and underscores (A-z, 0-9, and _ )
# A variable names are case-sensitive (age, Age, and AGE are three different variables)

# legal variable names

myvar = "abhi"
my_var = "abhi"
_my_var = "abhi"
myVar = "abhi"
MYVAR = "abhi"
myvar2 = "abhi"

